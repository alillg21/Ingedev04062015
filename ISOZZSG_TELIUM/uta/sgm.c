static int getTrk(char *trk,const S_TRANSIN *src)
{
	const char *ptr;
	char *dst;
	byte len;
	VERIFY(trk);
	VERIFY(src);
	memcpy(trk+0*128,src->track1,sizeof(src->track1));

	ptr= src->track2;
	len= 0;
	if(*ptr++!='B')
		return -1;
	dst= trk+1*128;
	while(*ptr!='F')
	{
		if(len>=128)
			return -1;
		if(*ptr=='D')
			*dst++= '=';
		else
			*dst++= *ptr;
		len++;
		ptr++;
	}
	memcpy(trk+2*128,src->track3,sizeof(src->track3));
	return 128*3;
}

