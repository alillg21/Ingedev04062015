void Task_Protocol(void)
{
    unsigned int   cr;          /* Result of ttestall                            */
    unsigned int   mask;        /* Mask of event to wait depending of task state */
    char           buf[128];    /* To read from serial link                      */
    char           data[1024];  /* To read from serial link                      */
    unsigned int   nb;          /* Result of fread                               */
    unsigned int   len = 0;     /* Length of data received                       */
    char 		   strAmount[20];
    char		   buf1[128];
    int ret;


    /* Open all protocole peripheral (for exemple COM0) */
    testHandle = fopen(ECR_COM_PORT,"rw");
    /* initialize peripheral */
    /* 9600 bauds,8 bits, 1 stop bit,no parity */
    format(ECR_COM_PORT,CBR_9600,8,1,NO_PARITY,NO_PARITY,0);
    /* We are now waiting for serial link and synchronization */
    mask = ECR_COM_EVENT;

    /* Task is now operationnal */
    SignalEvent(m2os_task,EVT_SYNCHRO_DIALOG);
    while (1)
    {
        cr = ttestall(mask,0);
        if( cr == ECR_COM_EVENT )
        {
            /*-------------------------------------------*/
            /* Put protocol management here              */
            /*          for exemple                      */
            /* Read from COM0 and print data received    */
            /*-------------------------------------------*/
            /* nothing */
            len = 0;
            do
            {
                nb=fread(buf,1,128,testHandle);
                if(nb)
                {
                    if ((len + nb) <= sizeof(data))
                    {
                        memcpy(&data[len],buf,nb);
                        len+=nb;
                    }
                    else
                    {
                        memcpy(&data[len],buf,sizeof(data)-len);
                        len = sizeof(data);
                    }
                }
            }
            while(((len != sizeof(data)) && (ttestall(ECR_COM_EVENT,10) == ECR_COM_EVENT)) || (nb != 0));

            if(len != 0)
            {
                /* Only synchronisation to wait */
                mask = SYNCHRO_DIALOG;
                /* Something has been received, request manager focus */
                IAM_to_myself();
            }
        }
        else // SYNCHRO_DIALOG event
        {
        	switch (data[0]) {
        	case 0x31:
				if ((data[1] == ';')){
					setECRFlag(1); //Set the ECRFlag transaction, so we can skip the amount Entry
					testInit = 1;
					memset(strAmount,0x00,sizeof(strAmount));
					memcpy(strAmount,data+2,len-2);
					MAPPUTSTR(appAmtECR, strAmount, lblKO);

					mask = SYNCHRO_DIALOG; //@agmr - event
                    SignalEvent(m2os_task,EVT_SYNCHRO_DIALOG); //@agmr - event
                    ttestall(mask,0);//@agmr - event

					//Check the response from host, collect the response value, and send to PC
					getInfoEcr(1, buf);
					nb = fwrite(buf,1,strlen(buf),testHandle);
				}
        		break;

			case 0x32:
				if ((data[1] == ';')){
					setECRFlag(1); //Set the ECRFlag transaction, so we can skip the amount Entry
					testInit = 2;
					memset(strAmount,0x00,sizeof(strAmount));
					memcpy(strAmount,data+2,len-2);
					MAPPUTSTR(appAmtECR, strAmount, lblKO);

					mask = SYNCHRO_DIALOG; //@agmr - event
                    SignalEvent(m2os_task,EVT_SYNCHRO_DIALOG); //@agmr - event
                    ttestall(mask,0);//@agmr - event

					getInfoEcr(2, buf);
					nb = fwrite(buf,1,strlen(buf),testHandle);
				}
				else if((data[1] == 0x33))
				{
					setECRFlag(1); //Set the ECRFlag transaction, so we can skip the amount Entry
					testInit = 6;

					//memset(strAmount,0x00,sizeof(strAmount));
					//memcpy(strAmount,data+2,len-2);
					MAPPUTSTR(appAmtECR, "1000", lblKO);

					mask = SYNCHRO_DIALOG; //@agmr - event
                    			SignalEvent(m2os_task,EVT_SYNCHRO_DIALOG); //@agmr - event
                    			ttestall(mask,0);//@agmr - event
					
					getInfoEcr(6, buf);
					nb = fwrite(buf,1,strlen(buf),testHandle);
				}
				break;

			case 0x33:
				if ((data[1] == ';')){
					setECRFlag(1); //Set the ECRFlag transaction, so we can skip the amount Entry
					testInit = 3;
					memset(strAmount,0x00,sizeof(strAmount));
					memcpy(strAmount,data+2,len-2);
					MAPPUTSTR(appAmtECR, strAmount, lblKO);

					mask = SYNCHRO_DIALOG; //@agmr - event
                    SignalEvent(m2os_task,EVT_SYNCHRO_DIALOG); //@agmr - event
                    ttestall(mask,0);//@agmr - event

					getInfoEcr(3, buf);
					nb = fwrite(buf,1,strlen(buf),testHandle);
				}	
				break;
				
			case 0x34:
				if(FnPrintRef()){
					memset(buf, 0, sizeof(buf));
					memset(buf1, 0, sizeof(buf1));
					strcpy(buf1, "print;");
					mapGet(traLWBP, buf, lenLWBP);
					strcat(buf1,buf);
					nb = fwrite(buf1,1,strlen(buf1),testHandle);
				}
				break;
				
			case 0x35:
				FnPrinthis();
				break;
        	}



            /* We are now waiting for serial link and synchronization */
            mask = ECR_COM_EVENT;

            /* Traitment is over */
            SignalEvent(m2os_task,EVT_SYNCHRO_DIALOG);
        }
    }
    fclose(testHandle);
	lblKO:
		return;
}

